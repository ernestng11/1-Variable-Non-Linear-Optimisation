%%**********************************************
%% newtonfun: evaluate f(x), df(x), d2f(x).
%% 
%% f(x) = exp(x) + x.*x - 2*x. 
%%
%%**********************************************

   function [f,df,d2f] = newtonfun(x,funparms); 
   if x>=0
       f   = 4*x^3 - 3*x^4;
       df  = 12*x^2 - 12*x^3; 
       d2f = 24*x - 36*x^2;  
   end
   if x<0
       f = 4*x^3 + 3*x^4;
       df = 12*x^2 + 12*x^3;
       d2f = 24*x + 36*x^2;
   end
   
